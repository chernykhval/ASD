#include <cmath>
#include "libs/lib_math/math.h"

double Math::sin(double x) {
    return std::sin(x);
}

double Math::cos(double x) {
    return std::cos(x);
}

double Math::tg(double x) {
    return std::tan(x);
}

double Math::sqrt(double x) {
    return std::sqrt(x);
}

double Math::ln(double x) {
    return std::log(x);
}
